// Function to get images for a given place folder
// Enhanced to support both picture and picture_1 directories
export const getPlaceImages = (pictureFolder) => {
  if (!pictureFolder || !imageMap[pictureFolder]) {
    return [];
  }
  
  // Check if it's a new picture_1 collection first
  const picture1Collections = [
    "Dalma wildlife santury", "Dumka", "Ghatshila", "Jubilee park", "Kanke dam",
    "Lodh fall", "Maluti temple", "Mayurbhanj palace", "Usri fall", "Hirni fall",
    "Jagarnath temple", "Rock garden ranchi", "Tata steel zoological", "Topchanchu lake"
  ];
  
  if (picture1Collections.includes(pictureFolder)) {
    return imageMap[pictureFolder].map(filename => `/picture_1/${pictureFolder}/${filename}`);
  }
  
  return imageMap[pictureFolder].map(filename => `/picture/${pictureFolder}/${filename}`);
};

// Function to get a single image for a place
export const getPlaceMainImage = (pictureFolder) => {
  const images = getPlaceImages(pictureFolder);
  return images.length > 0 ? images[0] : '/placeholder-image.jpg';
};

